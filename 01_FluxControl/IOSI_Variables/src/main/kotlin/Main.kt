fun main() {
    //Como escrever: Acessor, nome, tipo.
    var x: Int //Pode ser alterado (Variável mutável)
    val y: Int = 18 // Não pode ser alterado (Variável imutável)
    var a: Double = 2.4
    var b: Float = 3.14f
    var c: Long = 123157435
    var d: String = "Gabriel"
    var e: Boolean = true
    var f: Char = 'a'

    println(y)
    println(a)
    println(b)
    println(c)
    println(d)
    println(e)
    println(f)
}